public class Switch {
    public static void main(String args[]){
        int num = 3;

        switch(num){
            case 1: System.out.println("prints 1");
             break;
            case 2: System.out.println("prints 2");
            break;
            case 3 :System.out.println("prints 3");
            break;
            default : System.out.println("value not found");

        }
    }
}
